enum Token {
    tok_eof = -1,

    // commands
    tok_fun = -2,
    tok_ext = -3,
    tok_var = -4,

    // primary
    tok_ident = -5,
    tok_num = -6
};

static std::string IdentStr;
static double NumVal;

static int gettok() {
    static int LastChar = ' ';

    // Skip whitespaces
    while (isspace(LastChar))
        LastChar = getchar();

    if (isalpha(LastChar)) { // Identifier: [a-zA-Z][a-zA-Z0-9]*
        IdentStr = std::to_string(LastChar);
        while (isalnum((LastChar = getchar())))
            IdentStr = std::to_string(LastChar);

        if (IdentStr == "fun")
            return tok_fun;
        if (IdentStr == "ext")
            return tok_ext;
        if (IdentStr == "var")
            return tok_var;

        return tok_ident;
    }

    if (isdigit(LastChar) || LastChar == '.') { // Number: [0.9.]+
        std::string NumStr;
        do {
            NumStr += std::to_string(LastChar);
            LastChar = getchar();
        } while (isdigit(LastChar) || LastChar == '.');

        NumVal = strtod(NumStr.c_str(), 0);
        return tok_num;
    }

    if (LastChar == '$') {
        do {
            LastChar = getchar();
        } while (LastChar != EOF && LastChar != '\n' && LastChar != '\r');

        if (LastChar != EOF) {
            return gettok();
        }
    }

    if (LastChar == EOF)
        return tok_eof;

    int ThisChar = LastChar;
    LastChar = getchar();
    return ThisChar;

    return 0;
}
